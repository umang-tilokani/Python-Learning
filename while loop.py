# while loop
# WAP to print 1-10 numbers.
i = 1 # initialization
while i<=10: #condition
    print(i,end=" ")
    i=i+1 # increament
print("\n_____________________")
# WAP to print 10 numbers in reverse order with a difference of 2.
i = 20 # initialization
while i>0: # condition
    print(i,end=" ")
    i=i-2 #decreament